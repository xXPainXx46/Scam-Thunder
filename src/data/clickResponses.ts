export const getRandomClickResponse = (clicks: number): string => {
  const availableResponses = clickResponses.filter(response => {
    const meetsMin = !response.minClicks || clicks >= response.minClicks;
    const meetsMax = !response.maxClicks || clicks <= response.maxClicks;
    return meetsMin && meetsMax;
  });

  if (availableResponses.length === 0) {
    return "Кошелек пуст, но сердце полно!";
  }

  // Create weighted array
  const weightedResponses: string[] = [];
  availableResponses.forEach(response => {
    for (let i = 0; i < response.weight; i++) {
      weightedResponses.push(response.text);
    }
  });

  return weightedResponses[Math.floor(Math.random() * weightedResponses.length)];
};
